int add_unsigned(unsigned int a, unsigned int b, unsigned long c, unsigned long d,
                 unsigned int e, unsigned int f, unsigned long g, unsigned int h,
                 unsigned long i) {
    /* Make sure unsigned arguments are passed correctly */
    return (a == 1 && b == -1 && c == 2 && d == -2 && e == 3 && f == -3 &&
            g == 4 && h == -4 && i == 5);
}